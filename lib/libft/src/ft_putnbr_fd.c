#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	x;

	if (n == -2147483648)
	{
		write(fd, "-2147483648\n", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 0 && n <= 9)
	{
		x = n % 10 + 48;
		write(fd, &x, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		x = n % 10 + 48;
		write(fd, &x, 1);
	}
}
